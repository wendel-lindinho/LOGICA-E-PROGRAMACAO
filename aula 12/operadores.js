//Operadores Aritimeticos
let n1 = 5;
let n2 = 2;

//Opereador de soma

let soma = n1 + n2; 
console.log(soma);

// Operador de subtração
let = subtracao = n1 - n2;
console.log(subtracao);

// operador de mutiplicacao
let mutiplicacao = n1 * n2;
console.log(mutiplicacao);

// operador de divisao
let divisao = n1 / n2;
console.log (divisao);

//operador modulo
let modulo = n1 % n2;
console.log(modulo);

//operador potencia
let potencia = Math.pow(n1,n2);
console.log (potencia);

//operador raiz quadrada
let raizQuadrada = Math.sqrt(n1);
console.log(raizQuadrada);
